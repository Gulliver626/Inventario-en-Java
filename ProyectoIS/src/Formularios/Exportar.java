    public void exportar() {
        //selecciona carpeta donde guardar
        JFileChooser archivos = new JFileChooser();
        archivos.setDialogTitle("Seleccionar Carpeta");
        archivos.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int guardarscript = archivos.showSaveDialog(null);
        //elije ruta
        if (guardarscript == JFileChooser.APPROVE_OPTION) {
            String ruta = archivos.getSelectedFile().getPath();
            txtruta.setText(ruta);
        }
    }

    private void btn_respaldoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_respaldoActionPerformed
        String laruta = txtruta.getText();
        String nombrescripsql = "\\inventario.sql";
        String backup = "";
        //hacer respaldo
        try {
            if (laruta.trim().length() > 0) {
                backup = "C:\\xampp\\mysql\\bin\\mysqldump --opt -u" + con.getUsuario() + " -p " + con.getPassword() + " -B " + con.getBD() + " -r " + laruta + nombrescripsql;
                Runtime rtime = Runtime.getRuntime();
                rtime.exec(backup);
                JOptionPane.showMessageDialog(null, "La copia de seguridad de la base de datos fue exitosa", "Mensaje del sistrema", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione una Carpeta para guardar la copia de seguridad de la BDD", "Mensaje del sistema", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error ", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_respaldoActionPerformed

