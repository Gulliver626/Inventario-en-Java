    int intn = 0;
    private void PRECIOVENTAKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PRECIOVENTAKeyTyped
        char c = evt.getKeyChar();
        if (c == '.') {
            intn = intn + 1;
            if (intn == 1) {
                if ((c != '.') && (c < '0' || c > '9')) {
                    evt.consume();
                }
            } else if (intn > 1) {
                if (c < '0' || c > '9') {
                    evt.consume();
                }
            }
        }
    }//GEN-LAST:event_PRECIOVENTAKeyTyped

