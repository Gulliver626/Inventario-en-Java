    public void RealizarVenta() {
        VentasSQL ov = new VentasSQL();
        columnasTab us = new columnasTab();

        String fecha = String.valueOf(FECHAVENTA.getDate());

        // si no estan llenados los campos
        if (fecha.equals("null") || TXTPRODUCTO.getText().equals("") || TXTCOMPRADOR.getText().equals("")
                || fecha.equals("") || PRECIOVENTA.getText().equals("")
                || TXTPRODUCTO.getText().equals("Nombre del producto")
                || TXTPROVEEDOR.getText().equals("Nombre del proveedor")
                || PRECIOVENTA.getText().equals("$$$$") || CANTIDADVENTA.getText().equals("00000")
                || CANTIDADVENTA.getText().equals("")) {

            TXTERROR.setText("Completar todos los campos");

        } else {

            //guardar datos en el modelo columnasTab
            int numC = Integer.parseInt(CANTIDADVENTA.getText());
            int numPrecio = Integer.parseInt(PRECIOVENTA.getText());
            us.setProductoSALIDA(TXTPRODUCTO.getText());
            us.setCompradorVENTA(TXTCOMPRADOR.getText());
            us.setPrecio_ventaVENTA(numPrecio);
            us.setCantidadVENTA(numC);
            us.setFecha_ventaVENTA(FECHAVENTA.getDate());
            us.setProveedorCOMPRA(TXTPROVEEDOR.getText());

            //metodo Venta del modelo VentasSQL
            if (ov.Venta(us)) {
                limpiar();
                TXTERROR.setText("Venta Exitosa");
            } else {
                TXTERROR.setText("Error");
            }
        }
    }

