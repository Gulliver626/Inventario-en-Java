    private void Buscar() {

        BuscarProductoA bus = new BuscarProductoA();
        columnasTab usu = new columnasTab();

        //Mandamos el nombre del producto para ser buscado
        usu.setProductoINGRESARB(TXTPRODUCTO.getText());

        // si las cajas estan vacias
        if (TXTPRODUCTO.getText().equals("") || TXTPRODUCTO.getText().equals("Nombre del producto")
                || TXTPROVEEDOR.getText().equals("Nombre del proveedor")) {
            TXTERROR.setText("No ha ingresado el nombre del objeto a buscar");
        } else {

            //metodo InGRESARBUSQUEDA del modelo EntradasSQL
            if (bus.INGRESARBUSQUEDA(usu)) {
                //Iniciamos variables de tipo usuarios para que no sean nulas

                int Precio = usu.getPrecioINGRESARB();
                int Cantidad = usu.getCantidadINGRESARB();

                String Categoria = usu.getCategoriaINGRESARB();
                PRECIO.setText(String.valueOf(Precio));
                CANTIDAD.setText(String.valueOf(Cantidad));
                TXTCATEGORIA.setText(Categoria);
                String Producto = usu.getProductoINGRESARB();

                //Comparaciones
                if (Producto == null) {

                    TXTERROR.setText("Producto No Encontrado");

                } else {
                    TXTERROR.setText("Producto Encontrado");
                }

            } else {
                TXTERROR.setText("Producto No encontrado en Ineventario");
            }
        }
    }

