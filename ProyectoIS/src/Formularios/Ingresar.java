    private void IngresarProducto() {

        //formato de la fecha
        Date fe = FECHAINGRESO.getDate();
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        String X = df.format(fe);
        System.out.println(X);
        String fecha = String.valueOf(FECHAINGRESO.getDate());

        // si no estan llenados los campos
        if (fecha.equals("null") || TXTPRODUCTO.getText().equals("") || TXTPROVEEDOR.getText().equals("")
                || fecha == "" || PRECIO.getText().equals("")
                || CANTIDAD.getText().equals("") || TXTCATEGORIA.getText().equals("")
                || TXTPRODUCTO.getText().equals("Nombre del producto")
                || TXTPROVEEDOR.getText().equals("Nombre del proveedor")
                || PRECIO.getText().equals("$$$$") || CANTIDAD.getText().equals("00000")) {

            TXTERROR.setText("Completar todos los campos");

        } else {
            //metodo ProductoExistente del modelo EntradasSQL
            if (e.ProductoExistente(TXTPRODUCTO.getText(), TXTPROVEEDOR.getText()) == 0) {

                registros();

                //metodo Entradas del modelo EntradasSQL
                if (e.Entradas(us)) {
                    limpiar();
                } else {
                    TXTERROR.setText("Error al guardar Producto");
                }
                TXTERROR.setText("Producto Guardado");

            } else {

                TXTERROR.setText("Producto Existente");
                registros();

                //metodo Insertar del modelo EntradasSQL
                if (e.Insertar(us)) {
                    limpiar();
                    TXTERROR.setText("Producto Guardado");
                } else {
                    TXTERROR.setText("Error al guardar Producto");
                }

            }

        }
    }

    public void registros() {

        //guardar datos en el modelo columnasTab
        //----------variables
        int numPrecio = Integer.parseInt(PRECIO.getText());
        int numCantidad = Integer.parseInt(CANTIDAD.getText());
        int numSalida = 0;
        int Cantidad2 = us.getCantidadINGRESAR();
        int CTotal = Cantidad2 + numCantidad;
        //----------seters
        us.setProductoINGRESAR(TXTPRODUCTO.getText());
        us.setCategoriaINGRESAR(TXTCATEGORIA.getText());
        us.setSalidaINGRESAR(numSalida);
        us.setEstado_productoINGRESAR(ESTADOSPRODUCTO.getSelectedItem().toString());
        us.setPrecioINGRESAR(numPrecio);
        us.setCantidadINGRESAR(CTotal);
        us.setProveedorCOMPRA(TXTPROVEEDOR.getText());
        us.setFecha_entradaENTRADA(FECHAINGRESO.getDate());
    }

